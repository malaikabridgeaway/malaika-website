const LANGS = ["en", "es", "sw"];

function applyLang(lang) {
  if (!LANGS.includes(lang)) lang = "en";
  const t = I18N[lang];
  document.documentElement.lang = lang;
  document.title = t["meta.title"];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const s = t[el.dataset.i18n];
    if (s === undefined) return;
    if (el.namespaceURI === "http://www.w3.org/2000/svg" || !s.includes("<")) {
      // decode entities for plain-text nodes
      const tmp = document.createElement("textarea");
      tmp.innerHTML = s;
      el.textContent = tmp.value;
    } else {
      el.innerHTML = s;
    }
  });
  document.querySelectorAll("[data-i18n-aria]").forEach(el => {
    const s = t[el.dataset.i18nAria];
    if (s !== undefined) el.setAttribute("aria-label", s);
  });
  document.querySelectorAll(".lang-btn").forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle("active", active);
    btn.setAttribute("aria-pressed", active);
  });
  try { localStorage.setItem("mbaf-lang", lang); } catch (e) { /* private mode */ }
}

function initialLang() {
  const fromUrl = new URLSearchParams(location.search).get("lang");
  if (fromUrl && LANGS.includes(fromUrl)) return fromUrl;
  try {
    const saved = localStorage.getItem("mbaf-lang");
    if (saved && LANGS.includes(saved)) return saved;
  } catch (e) { /* private mode */ }
  const nav = (navigator.language || "en").toLowerCase();
  if (nav.startsWith("es")) return "es";
  if (nav.startsWith("sw")) return "sw";
  return "en";
}

document.querySelectorAll(".lang-btn").forEach(btn =>
  btn.addEventListener("click", () => applyLang(btn.dataset.lang))
);
applyLang(initialLang());
